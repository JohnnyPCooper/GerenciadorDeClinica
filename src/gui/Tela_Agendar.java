    public Tela_Agendar(int nivel_acesso, int id_conta, String nome) {
        initComponents();
        jPanel2.setEnabled(false);
        jTable1.setEnabled(false);
        jButton3.setEnabled(false);
        this.nivel_acesso = nivel_acesso;
        this.id_conta = id_conta;
        this.nome = nome;
        jTextField1.setText(nome);
        DefaultTableModel tabela = (DefaultTableModel) jTable1.getModel();
        MedicoManager pessoaTab = new MedicoManager();
        List<Medico> pessoas = new ArrayList<Medico>();
        pessoas = pessoaTab.listar();
        for(int i = 0; i < pessoas.size(); i++){
            Pessoa paux = pessoas.get(i);
            Object[] dados = {paux.getId(), paux.getNome()};
            tabela.addRow(dados);
        }
    }

