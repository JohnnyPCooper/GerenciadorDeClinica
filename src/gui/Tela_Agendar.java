    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        AgendaManager agenda = new AgendaManager();
        Agendar agendar = new Agendar();
        agendar.setData(String.valueOf(jFormattedTextField1.getText()));
        agendar.setHorario(String.valueOf(jFormattedTextField2.getText()));
        agendar.setId_medico(id_medico);
        agendar.setId_paciente(id_conta);
        try {
            agenda.inserir(agendar);
            Tela_Agenda agendaa = new Tela_Agenda(nivel_acesso);
            agendaa.setVisible(true);
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Agendar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tela_Agendar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

