    private void botao_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_entrarActionPerformed
        ContasManager contasLogin = new ContasManager();
        Contas conta = new Contas();
        conta.setLogin(campo_login.getText());
        conta.setSenha(jPasswordField1.getText());
        boolean podeEntrar = contasLogin.autenticar(conta);
        conta.setNivel_acesso(contasLogin.getNivel_acesso());
        int perms = conta.getNivel_acesso();
        System.out.print(perms);
        if(podeEntrar){
            Tela_Menu menu = new Tela_Menu(perms);
            menu.setVisible(true);
            dispose();
            //Verifica notification = new Verifica(String.valueOf(campo_login.getText()));
            Verificando notification = new Verificando(String.valueOf(campo_login.getText()));
            Thread notifications = new Thread(notification);
            notifications.start();
        }
    }//GEN-LAST:event_botao_entrarActionPerformed

