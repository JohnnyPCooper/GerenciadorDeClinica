    private void campo_buscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campo_buscaActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) jTable1.getModel();
        PessoaManager pessoaEspec = new PessoaManager();
        List<Pessoa> pessoas = new ArrayList<Pessoa>();
        pessoas = pessoaEspec.selecionar(campo_busca.getText());
        int tamanho = tabela.getRowCount();
        for(int i = 0; i < tamanho; i++){
            tabela.removeRow(i);
        }
        for(int i = 0; i < pessoas.size(); i++){
            
            Pessoa paux = pessoas.get(i);
            Object[] dados = {paux.getId(), paux.getNome(), paux.getSobrenome(), paux.getTelefone(), paux.getEndereco(), paux.getDoencas(), paux.getNascimento(), paux.getConvenio()};
            tabela.addRow(dados);
        }
        
    }//GEN-LAST:event_campo_buscaActionPerformed

