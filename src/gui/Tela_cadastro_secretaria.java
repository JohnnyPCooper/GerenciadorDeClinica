    private void botao_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_cadastrarActionPerformed

            System.out.println("1");
            PessoaManager secretariaCad = new PessoaManager();
            Pessoa secretaria = new Pessoa();
            secretaria.setNome(campo_nome.getText());
            secretaria.setSobrenome(campo_sobrenome.getText());
            secretaria.setTelefone(campo_telefone.getText());
        try {
            secretariaCad.inserir(secretaria);
        } catch (SQLException ex) {
            Logger.getLogger(Tela_cadastro_secretaria.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tela_cadastro_secretaria.class.getName()).log(Level.SEVERE, null, ex);
        }
            System.out.println("1");
            ComandosManager comando = new ComandosManager();
            Pessoa paux = new Pessoa();
            paux = comando.selecionar(secretaria.getTelefone());
            System.out.println(paux.getNome());
            Contas conta = new Contas();
            conta.setLogin(Integer.toString(paux.getId()));
            conta.setId_conta(paux.getId());
            conta.setSenha(campo_senha.getText());
            ContasManager contas = new ContasManager();
        try {
            contas.cadastrar(conta);
        } catch (SQLException ex) {
            Logger.getLogger(Tela_cadastro_secretaria.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tela_cadastro_secretaria.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_botao_cadastrarActionPerformed

